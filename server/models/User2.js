const { Schema, model } = require('mongoose');
const bcrypt = require('bcrypt');

const userSchema2 = new Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
      trim: true
    },
    email: {
      type: String,
      required: true,
      unique: true,
      match: [/.+@.+\..+/, 'Must match an email address!']
    },
    password: {
      type: String,
      required: true,
      minlength: 5
    },
    thoughts: [
      {
        type: Schema.Types.ObjectId,
        ref: 'Thought'
      }
    ],
    friends: [
      {
        type: Schema.Types.ObjectId,
        ref: 'User'
      }
    ],
    paydetails: [
      {
        type: Schema.Types.ObjectId,
        ref: 'PayDetails'
      }
    ],
  },
  {
    toJSON: {
      virtuals: true
    }
  }
);

